    function fibonacci(n) {
        let fib = [0, 1];
        for (let i = 2; i <= n; i++) {
            fib[i] = fib[i - 1] + fib[i - 2];
        }
        return fib[n];
    }
    
    function isPrime(num) {
        if (num <= 1) return false;
        if (num <= 3) return true;
        if (num % 2 === 0 || num % 3 === 0) return false;
        let i = 5;
        while (i * i <= num) {
            if (num % i === 0 || num % (i + 2) === 0) return false;
            i += 6;
        }
        return true;
    }
    
    function findLargestPrimeFibonacci(limit) {
        let maxPrime = -Infinity;
        let n = 0;
        while (true) {
            let fib = fibonacci(n);
            if (fib > limit) break;
            if (isPrime(fib) && fib > maxPrime) {
                maxPrime = fib;
            }
            n++;
        }
        return maxPrime;
    }
    
